const fs = require('fs');

// Create a simple 1x1 transparent PNG (valid minimal PNG)
const createMinimalPNG = () => {
  return Buffer.from([
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
    0x00, 0x00, 0x00, 0x0D, // IHDR chunk length
    0x49, 0x48, 0x44, 0x52, // IHDR
    0x00, 0x00, 0x00, 0x01, // width: 1
    0x00, 0x00, 0x00, 0x01, // height: 1
    0x08, 0x06, 0x00, 0x00, 0x00, // bit depth, color type, compression, filter, interlace
    0x1F, 0x15, 0xC4, 0x89, // CRC
    0x00, 0x00, 0x00, 0x0A, // IDAT chunk length
    0x49, 0x44, 0x41, 0x54, // IDAT
    0x78, 0x9C, 0x62, 0x00, 0x00, 0x00, 0x02, 0x00, 0x01, 0xE2, // compressed data
    0x21, 0xBC, 0x33, // CRC
    0x00, 0x00, 0x00, 0x00, // IEND chunk length
    0x49, 0x45, 0x4E, 0x44, // IEND
    0xAE, 0x42, 0x60, 0x82  // CRC
  ]);
};

// Create a simple ICO file (16x16)
const createMinimalICO = () => {
  return Buffer.from([
    0x00, 0x00, // Reserved
    0x01, 0x00, // Type: ICO
    0x01, 0x00, // Number of images
    0x10, 0x10, 0x00, 0x00, 0x01, 0x00, 0x20, 0x00, // Image directory entry
    0x68, 0x04, 0x00, 0x00, 0x16, 0x00, 0x00, 0x00, // Size and offset
    // Bitmap header and minimal data (simplified)
    ...Array(1128).fill(0) // Minimal bitmap data
  ]);
};

try {
  // Create minimal valid files
  const pngData = createMinimalPNG();
  const icoData = createMinimalICO();
  
  fs.writeFileSync('client/public/favicon.ico', icoData);
  fs.writeFileSync('client/public/logo192.png', pngData);
  fs.writeFileSync('client/public/logo512.png', pngData);
  
  console.log('✅ Minimal valid logo files created!');
} catch (error) {
  console.error('❌ Error:', error.message);
}